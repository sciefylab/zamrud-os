//! Zamrud OS - Terminal Font
//! Modern 8x16 bitmap font - JetBrains Mono inspired
//! Clean, readable, with better spacing and curves

pub const FONT_WIDTH: u32 = 8;
pub const FONT_HEIGHT: u32 = 16;

/// Get character bitmap (16 bytes per character)
pub fn getChar(c: u8) []const u8 {
    if (c < 32 or c > 126) {
        return font_data[0..16]; // Return space for invalid
    }
    const index: usize = c - 32;
    const start = index * 16;
    return font_data[start .. start + 16];
}

/// Get box drawing character (for UI elements)
pub fn getBoxChar(code: u8) []const u8 {
    return switch (code) {
        0x00 => &box_horizontal,
        0x01 => &box_vertical,
        0x02 => &box_top_left,
        0x03 => &box_top_right,
        0x04 => &box_bottom_left,
        0x05 => &box_bottom_right,
        0x06 => &box_t_down,
        0x07 => &box_t_up,
        0x08 => &box_t_right,
        0x09 => &box_t_left,
        0x0A => &box_cross,
        else => font_data[0..16],
    };
}

