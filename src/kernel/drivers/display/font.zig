// =============================================================================
// T1.0: Font Glyph Lookup — Unified API
// =============================================================================

/// Get glyph bitmap for any supported codepoint (16 bytes per glyph)
/// Supports: ASCII 0x20-0x7E, Latin-1 0x80-0xFF, Unicode box drawing, blocks, symbols
pub fn getGlyph(codepoint: u21) []const u8 {
    // ASCII printable range (0x20 - 0x7E)
    if (codepoint >= 0x20 and codepoint <= 0x7E) {
        const index: usize = codepoint - 0x20;
        const start = index * 16;
        return ascii_glyphs[start .. start + 16];
    }

    // Latin-1 supplement (0x80 - 0xFF) — accented chars, symbols
    if (codepoint >= 0xA0 and codepoint <= 0xFF) {
        const index: usize = codepoint - 0xA0;
        const start = index * 16;
        return latin1_glyphs[start .. start + 16];
    }

    // Box drawing characters (U+2500 - U+257F)
    if (codepoint >= 0x2500 and codepoint <= 0x257F) {
        return getBoxDrawingGlyph(codepoint);
    }

    // Block elements (U+2580 - U+259F)
    if (codepoint >= 0x2580 and codepoint <= 0x259F) {
        return getBlockElementGlyph(codepoint);
    }

    // Common symbols
    if (getSymbolGlyph(codepoint)) |g| {
        return g;
    }

    // Arrow symbols (U+2190 - U+21D5)
    if (getArrowGlyph(codepoint)) |g| {
        return g;
    }

    // Fallback: replacement character (filled rectangle)
    return &glyph_replacement;
}

/// Legacy API compatibility — get ASCII character bitmap
pub fn getChar(c: u8) []const u8 {
    return getGlyph(@as(u21, c));
}

/// Legacy API compatibility — get box drawing by old code
pub fn getBoxChar(code: u8) []const u8 {
    return switch (code) {
        0x00 => getGlyph(0x2500), // ─
        0x01 => getGlyph(0x2502), // │
        0x02 => getGlyph(0x250C), // ┌
        0x03 => getGlyph(0x2510), // ┐
        0x04 => getGlyph(0x2514), // └
        0x05 => getGlyph(0x2518), // ┘
        0x06 => getGlyph(0x252C), // ┬
        0x07 => getGlyph(0x2534), // ┴
        0x08 => getGlyph(0x251C), // ├
        0x09 => getGlyph(0x2524), // ┤
        0x0A => getGlyph(0x253C), // ┼
        else => &glyph_replacement,
    };
}

/// Check if a codepoint has a glyph defined
pub fn hasGlyph(codepoint: u21) bool {
    if (codepoint >= 0x20 and codepoint <= 0x7E) return true;
    if (codepoint >= 0xA0 and codepoint <= 0xFF) return true;
    if (codepoint >= 0x2500 and codepoint <= 0x257F) return true;
    if (codepoint >= 0x2580 and codepoint <= 0x259F) return true;
    if (getSymbolGlyph(codepoint) != null) return true;
    if (getArrowGlyph(codepoint) != null) return true;
    return false;
}

/// Get total number of defined glyphs
pub fn getGlyphCount() u32 {
    return 95 + // ASCII printable
        96 + // Latin-1 supplement
        48 + // Box drawing (subset)
        16 + // Block elements (subset)
        12 + // Symbols
        6; // Arrows
}

// =============================================================================
// T1.1: Bold and Style Support
// =============================================================================

/// Get glyph with bold effect (OR with 1-pixel right shift)
pub fn getGlyphBold(codepoint: u21) [16]u8 {
    const base = getGlyph(codepoint);
    var bold: [16]u8 = undefined;
    for (0..16) |i| {
        bold[i] = base[i] | (base[i] >> 1);
    }
    return bold;
}

/// Get underline mask (row 14 filled)
pub fn getUnderlineMask() [16]u8 {
    var mask = [_]u8{0} ** 16;
    mask[13] = 0xFF; // Underline at row 13 (near bottom)
    return mask;
}

/// Get strikethrough mask (row 7-8 filled)
pub fn getStrikethroughMask() [16]u8 {
    var mask = [_]u8{0} ** 16;
    mask[7] = 0xFF;
    return mask;
}

